package model;

/**
 *
 * @author theph
 */
public class Product {
    int ID;
    String name,img;
    int type,quantity;
    double price;
    public Product() {
    }

    public Product(int ID, String name, String img, int quantity, double price, int type) {
        this.ID = ID;
        this.name = name;
        this.img = img;
        this.type = type;
        this.quantity = quantity;
        this.price = price;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

   
   

   
    

    
}
